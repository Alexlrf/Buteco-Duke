    private void jButtonIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIncluirActionPerformed
       
        SalgadoVO salgadoVO = new SalgadoVO();
        SalgadoController salgadoController = new SalgadoController();
        
        salgadoVO.setCategoria(jComboBoxIncluiCategoria.getSelectedItem().toString());
        salgadoVO.setSabor(jComboBoxIncluiSabor.getSelectedItem().toString());
        salgadoVO.setQuantidade(Integer.parseInt(jTextFieldIncluiQtde.getText()));
        salgadoVO.setValor(Double.parseDouble(jTextFieldIncluiValor.getText()));
        boolean retorno = salgadoController.saveSalgado(salgadoVO);
        
        if (retorno) {
            JOptionPane.showMessageDialog(rootPane, "Produto Incluído com sucesso!");
                jComboBoxIncluiCategoria.setSelectedIndex(0);
                jComboBoxIncluiSabor.setSelectedIndex(0);
                jTextFieldIncluiQtde.setText("");
                jTextFieldIncluiValor.setText("");        
                        
        } else {
            JOptionPane.showMessageDialog(rootPane, "Erro! Produto NÃO incluído");            
        }
        
    }//GEN-LAST:event_jButtonIncluirActionPerformed

