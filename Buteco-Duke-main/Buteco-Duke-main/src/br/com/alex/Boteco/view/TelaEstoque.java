    private void jButtonConsultaComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultaComboBoxActionPerformed
        
        int clearTabela = listaSalgado.getRowCount()-1;
                while(clearTabela != -1){
                    listaSalgado.removeRow(clearTabela);
                    clearTabela--;
                }
        
        
        List<SalgadoVO> listaCategoria = new ArrayList<>();
        List<SalgadoVO> listaSabor = new ArrayList<>();
        List<SalgadoVO> listaAmbos = new ArrayList<>();
        SalgadoVO salgadoVO = new SalgadoVO();
        SalgadoController salgadoController = new SalgadoController();        
        
        if (jComboBoxConsultaCategoria.getSelectedItem()!= "CATEGORIA" 
                && jComboBoxConsultaSabor.getSelectedItem() == "SABOR") {
            salgadoVO.setCategoria(jComboBoxConsultaCategoria.getSelectedItem().toString()); 
             listaCategoria = salgadoController.findByCategoria(salgadoVO);
               for (int i = 0; i < listaCategoria.size(); i++) {
                   listaSalgado.addRow(listaCategoria.get(i));                
               }
            }else if(jComboBoxConsultaSabor.getSelectedItem()!= "SABOR" 
                    && jComboBoxConsultaCategoria.getSelectedItem() == "CATEGORIA") {
                   salgadoVO.setSabor(jComboBoxConsultaSabor.getSelectedItem().toString()); 
                    listaSabor = salgadoController.findBySabor(salgadoVO);
                         for (int i = 0; i < listaSabor.size(); i++) {
                            listaSalgado.addRow(listaSabor.get(i));
                         }
            }else if(jComboBoxConsultaCategoria.getSelectedItem()!= "CATEGORIA" 
                    && jComboBoxConsultaSabor.getSelectedItem()!= "SABOR"){
                  salgadoVO.setCategoria(jComboBoxConsultaCategoria.getSelectedItem().toString());
                  salgadoVO.setSabor(jComboBoxConsultaSabor.getSelectedItem().toString());
                   listaAmbos = salgadoController.findByCategoriaSabor(salgadoVO);
                        for (int i = 0; i < listaAmbos.size(); i++) {
                           listaSalgado.addRow(listaAmbos.get(i));
                        }
            }
    }//GEN-LAST:event_jButtonConsultaComboBoxActionPerformed

